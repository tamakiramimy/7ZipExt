    [RelayCommand]
    private async Task BrowseArchive()
    {
        if (BrowseArchiveFolderAction == null) return;
        var folder = await BrowseArchiveFolderAction();
        if (folder == null) return;

        var fileName = Path.GetFileName(ArchivePath);
        if (string.IsNullOrWhiteSpace(fileName) || !Path.HasExtension(fileName))
            fileName = GetSuggestedArchiveFileName();

        SetArchivePath(Path.Combine(folder.Path.LocalPath, fileName));
    }

    [RelayCommand]
    private async Task AddSourceFiles()
    {
        if (PickFilesAction == null) return;
        var files = await PickFilesAction("选择要压缩的文件");
        if (files == null) return;
        AddSourceItems(files.Select(f => f.Path.LocalPath));
        ProgressText = $"已添加 {files.Count} 个文件。";
    }

    [RelayCommand]
    private async Task AddSourceFolders()
    {
        if (PickFoldersAction == null) return;
        var folders = await PickFoldersAction("选择要压缩的文件夹");
        if (folders == null) return;
        AddSourceItems(folders.Select(folder => folder.Path.LocalPath));
        ProgressText = $"已添加 {folders.Count} 个文件夹。";
    }

    [RelayCommand]
    private void RemoveSourceItems()
    {
        var toRemove = SelectedSources.ToList();
        foreach (var item in toRemove)
            SourceItems.Remove(item);
        SelectedSources.Clear();

        if (SourceItems.Count == 0 && _archivePathAutoGenerated)
            SetArchivePath(string.Empty);
        else
            UpdateDefaultArchivePath();
    }

    [RelayCommand]
    private async Task AddExclusionFiles()
    {
        if (PickFilesAction == null) return;
        var files = await PickFilesAction("选择要排除的文件");
        if (files == null) return;
        foreach (var f in files)
            AddExclusionItem(new ExclusionItem { Path = f.Path.LocalPath, IsFolder = false, IsRecursive = true });
    }

    [RelayCommand]
    private async Task AddExclusionFolder()
    {
        if (PickFoldersAction == null) return;
        var folders = await PickFoldersAction("选择要排除的文件夹");
        if (folders == null) return;
        foreach (var folder in folders)
            AddExclusionItem(new ExclusionItem { Path = folder.Path.LocalPath, IsFolder = true, IsRecursive = true });
    }

    [RelayCommand]
    private void AddExclusionPattern()
    {
        var pattern = PatternInput.Trim();
        if (string.IsNullOrWhiteSpace(pattern)) return;
        AddExclusionItem(new ExclusionItem { Path = pattern, IsFolder = false, IsRecursive = true });
        PatternInput = "";
    }

    [RelayCommand]
    private void RemoveExclusions()
    {
        var toRemove = SelectedExclusions.ToList();
        foreach (var item in toRemove) Exclusions.Remove(item);
        SelectedExclusions.Clear();
    }

    [RelayCommand]
    private async Task Compress()
    {
        if (SourceItems.Count == 0)
        {
            ProgressText = "请先添加至少一个要压缩的文件或文件夹。";
            return;
        }

        if (string.IsNullOrWhiteSpace(ArchivePath))
        {
            ProgressText = "请先确认保存文件路径。";
            return;
        }

        if (!File.Exists(_settings.Settings.SevenZipPath))
        {
            ProgressText = $"7-Zip 路径无效：{_settings.Settings.SevenZipPath}";
            return;
        }

        if (!string.IsNullOrWhiteSpace(Password) && Password != PasswordConfirm)
        {
            ProgressText = "错误：两次输入的密码不一致！";
            return;
        }

        if (!IsVolumeInputValid(out var volumeError))
        {
            ProgressText = volumeError;
            return;
        }

        IsRunning = true;
        ProgressText = "正在压缩...";
        try
        {
            var opts = BuildOptions();
            var progress = new Progress<string>(line => ProgressText = line);
            var (success, _, error) = await _sevenZip.CompressAsync(opts, progress);
            ProgressText = success ? "✅ 压缩完成！" : $"❌ 失败: {error}";

            if (success)
            {
                ExitApplicationAction?.Invoke();
            }
        }
        finally
        {
            IsRunning = false;
        }
    }

    [RelayCommand]
