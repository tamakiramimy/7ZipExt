        // Build thread options: Auto, then 1..max(cpuCount, 8)
        int cpuCount = Environment.ProcessorCount;
        ThreadOptions.Add("自动");
        int maxT = Math.Max(cpuCount, 8);
        for (int i = 1; i <= maxT; i++) ThreadOptions.Add(i.ToString());
        // Extend to common round numbers above cpu count
        foreach (int extra in new[] { 16, 24, 32, 48, 64 })
            if (extra > maxT) ThreadOptions.Add(extra.ToString());

        foreach (var preset in _volumePresets)
            VolumePresets.Add(preset);

        // Initialize all dependent combos
        ApplyFormat(_selectedFormat);
        SelectedVolumePreset = VolumePresets.FirstOrDefault() ?? "";
        SyncVolumeInput();
    }

    // ===== Format change cascade =====

    private void ApplyFormat(string format)
    {
        if (_updating) return;
        _updating = true;

        Methods.Clear();
        string[] methodList = format switch
        {
            "7z"    => new[] { "LZMA2", "LZMA", "PPMd", "BZip2", "Copy" },
            "zip"   => new[] { "Deflate", "Deflate64", "BZip2", "LZMA", "PPMd", "Copy" },
            "gzip"  => new[] { "Deflate", "Copy" },
            "bzip2" => new[] { "BZip2", "Copy" },
            "xz"    => new[] { "LZMA2", "Copy" },
            "tar"   => new[] { "Copy" },
            "wim"   => new[] { "Copy" },
            _        => Array.Empty<string>()
        };
        foreach (var m in methodList) Methods.Add(m);

        // Solid block size
        IsSolidEnabled = format == "7z" || format == "xz";
        SolidBlockSizes.Clear();
        if (IsSolidEnabled)
            foreach (var s in _solidBlockSizes) SolidBlockSizes.Add(s);

        // Encryption
        EncryptionMethodOptions.Clear();
        IsEncryptionEnabled = format == "7z" || format == "zip";
        IsEncryptFileNamesEnabled = format == "7z";
        if (format == "7z")
            EncryptionMethodOptions.Add("AES-256");
        else if (format == "zip")
        { EncryptionMethodOptions.Add("AES-256"); EncryptionMethodOptions.Add("ZipCrypto"); }
        if (EncryptionMethodOptions.Count > 0 && !EncryptionMethodOptions.Contains(SelectedEncryption))
            SelectedEncryption = EncryptionMethodOptions[0];

        string defaultMethod = format switch
        {
            "7z"    => "LZMA2",
            "zip"   => "Deflate",
            "gzip"  => "Deflate",
            "bzip2" => "BZip2",
            "xz"    => "LZMA2",
            _       => "Copy"
        };

        _updating = false;

        SelectedMethod = Methods.Contains(defaultMethod) ? defaultMethod
                       : Methods.Count > 0 ? Methods[0] : "";

        // Solid default
        if (IsSolidEnabled)
            SelectedSolidBlockSize = SolidBlockSizes.Contains("固实") ? "固实" : "";
        else
            SelectedSolidBlockSize = "";

        RefreshControlStates();
    }

    // ===== Method change cascade =====

    private void ApplyMethod(string method)
    {
        if (_updating) return;
        _updating = true;

        DictionarySizes.Clear();
        WordSizes.Clear();

        string[]? dictSrc = method switch
        {
            "LZMA" or "LZMA2"          => _lzmaDictSizes,
            "PPMd"                     => _ppmdDictSizes,
            "BZip2"                    => _bzip2DictSizes,
            "Deflate"                  => new[] { "32 KB" },
            "Deflate64"                => new[] { "64 KB" },
            _                          => null
        };
        if (dictSrc != null) foreach (var d in dictSrc) DictionarySizes.Add(d);

        string[]? wordSrc = method switch
        {
            "LZMA" or "LZMA2"          => _lzmaWordSizes,
            "PPMd"                     => _ppmdWordSizes,
            "Deflate" or "Deflate64"   => _deflateWordSizes,
            _                          => null
        };
        if (wordSrc != null) foreach (var w in wordSrc) WordSizes.Add(w);

        // Limit thread options by method
        RebuildThreadOptionsForMethod(method);

        _updating = false;

        ApplyLevelDefaults(SelectedLevel, method);
        RefreshControlStates();
    }

    private void RebuildThreadOptionsForMethod(string method)
    {
        // Max threads per method (from 7-zip SetNumThreads2)
        int maxForMethod = method switch
        {
            "LZMA"                                        => 2,
            "PPMd" or "Deflate" or "Deflate64" or "Copy" => 1,
            _                                             => Math.Min(Environment.ProcessorCount, 64)
        };

        ThreadOptions.Clear();
        ThreadOptions.Add("自动");
        int maxT = Math.Max(maxForMethod, 1);
        for (int i = 1; i <= maxT; i++) ThreadOptions.Add(i.ToString());

        // Keep SelectedThreads valid
        if (!ThreadOptions.Contains(SelectedThreads))
            SelectedThreads = "自动";

        IsThreadCountEnabled = ThreadOptions.Count > 1;
    }

    // ===== Level defaults cascade =====

    private void ApplyLevelDefaults(string level, string method)
    {
        if (string.IsNullOrWhiteSpace(method)) return;

        int lvl = LevelToInt(level);

        if (method is "LZMA" or "LZMA2")
        {
            // _auto_Dict = level<=4 ? 1<<(lvl*2+16) : 1<<(lvl+20)  (64-bit)
            long autoDict = lvl <= 4
                ? (1L << (lvl * 2 + 16))
                : (1L << (lvl + 20));
            string dictDefault = ClosestDictSize(DictionarySizes, autoDict);
            string wordDefault = lvl < 7 ? "32" : "64";

            SelectedDictionarySize = dictDefault;
            SelectedWordSize = WordSizes.Contains(wordDefault) ? wordDefault
                             : WordSizes.Count > 0 ? WordSizes[0] : "";
        }
        else if (method == "PPMd")
        {
            // _auto_Dict = 1<<(lvl+19)
            long autoDict = 1L << (lvl + 19);
            string dictDefault = ClosestDictSize(DictionarySizes, autoDict);
            string wordDefault = lvl >= 9 ? "32" : lvl >= 7 ? "16" : lvl >= 5 ? "6" : "4";

            SelectedDictionarySize = dictDefault;
            SelectedWordSize = WordSizes.Contains(wordDefault) ? wordDefault
                             : WordSizes.Count > 0 ? WordSizes[0] : "";
        }
        else if (method == "BZip2")
        {
            string dictDefault = lvl >= 5 ? "900 KB" : lvl >= 3 ? "500 KB" : "100 KB";
            SelectedDictionarySize = DictionarySizes.Contains(dictDefault)
                ? dictDefault : DictionarySizes.Count > 0 ? DictionarySizes[0] : "";
            SelectedWordSize = "";
        }
        else if (method is "Deflate" or "Deflate64")
        {
            SelectedDictionarySize = DictionarySizes.Count > 0 ? DictionarySizes[0] : "";
            string wordDefault = lvl >= 9 ? "128" : lvl >= 7 ? "64" : "32";
            SelectedWordSize = WordSizes.Contains(wordDefault) ? wordDefault
                             : WordSizes.Count > 0 ? WordSizes[0] : "";
        }
        else
        {
            SelectedDictionarySize = "";
            SelectedWordSize = "";
        }

        UpdateMemoryUsage();
    }

    private static string ClosestDictSize(ObservableCollection<string> sizes, long targetBytes)
    {
        if (sizes.Count == 0) return "";
        string best = sizes[0];
        long bestDiff = long.MaxValue;
        foreach (var s in sizes)
        {
            long bytes = ParseSizeBytes(s);
            long diff = Math.Abs(bytes - targetBytes);
            if (diff < bestDiff) { bestDiff = diff; best = s; }
        }
        return best;
    }

    // ===== Memory usage =====

    private void UpdateMemoryUsage()
    {
        var (comp, decomp) = CalculateMemory();
        MemoryCompress   = comp   > 0 ? $"{comp} MB"   : "—";
        MemoryDecompress = decomp > 0 ? $"{decomp} MB" : "—";
    }

    private (long compress, long decomp) CalculateMemory()
    {
        string method = SelectedMethod;
        if (string.IsNullOrWhiteSpace(method) || method == "Copy") return (1, 1);

        long dictMB = ParseSizeBytes(SelectedDictionarySize) / (1024 * 1024);
        if (dictMB < 1) dictMB = 1;

        int threads = int.TryParse(SelectedThreads, out var t) ? t : Environment.ProcessorCount;
        if (threads < 1) threads = 1;

        return method switch
        {
            "LZMA"   => (dictMB * 3 + 10, dictMB + 4),
            "LZMA2"  => ((long)Math.Ceiling(threads / 2.0) * (dictMB * 3) + 10, dictMB + 4),
            "PPMd"   => (dictMB + 4, dictMB + 4),
            "BZip2"  => (10, 4),
            "Deflate" or "Deflate64" => (2, 2),
            _        => (1, 1)
        };
    }

