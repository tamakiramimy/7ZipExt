    public void AddSourceItems(IEnumerable<string> items)
    {
        bool added = false;
        foreach (var item in items)
        {
            if (SourceItems.Contains(item)) continue;
            SourceItems.Add(item);
            added = true;
        }

        if (added) UpdateDefaultArchivePath();
    }

    public void SetSelectedSourceItems(IEnumerable<string> items)
    {
        SelectedSources.Clear();
        foreach (var item in items)
            SelectedSources.Add(item);
    }

    public void SetSelectedExclusions(IEnumerable<ExclusionItem> items)
    {
        SelectedExclusions.Clear();
        foreach (var item in items)
            SelectedExclusions.Add(item);
    }

    private void UpdateDefaultArchivePath()
    {
        if (SourceItems.Count == 0) return;
        if (!_archivePathAutoGenerated && !string.IsNullOrWhiteSpace(ArchivePath)) return;

        var first = SourceItems[0];
        bool isFile = File.Exists(first);
        var dir = isFile
            ? Path.GetDirectoryName(first) ?? ""
            : Path.GetDirectoryName(first.TrimEnd('\\', '/')) ?? "";
        var archiveName = GetSuggestedArchiveFileName(first, isFile);
        SetArchivePath(Path.Combine(dir, archiveName));
    }

    private string GetSuggestedArchiveFileName(string? sourcePath = null, bool? isFile = null)
    {
        var first = sourcePath ?? SourceItems.FirstOrDefault() ?? "archive";
        bool currentIsFile = isFile ?? File.Exists(first);
        var name = Path.GetFileNameWithoutExtension(currentIsFile ? first : first.TrimEnd('\\', '/'));
        var ext = SelectedFormat switch
        {
            "gzip"  => "gz",
            "bzip2" => "bz2",
            _       => SelectedFormat
        };

        if (string.IsNullOrWhiteSpace(name))
            name = "archive";

        return $"{name}.{ext}";
    }

    private void SetArchivePath(string path)
    {
        _isUpdatingArchivePath = true;
        ArchivePath = path;
        _archivePathAutoGenerated = true;
        _isUpdatingArchivePath = false;
    }

