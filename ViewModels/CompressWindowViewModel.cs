    private static long ParseSizeBytes(string display)
    {
        if (string.IsNullOrWhiteSpace(display)) return 0;
        var parts = display.Trim().Split(' ');
        if (parts.Length != 2 || !long.TryParse(parts[0], out long num)) return 0;
        return parts[1].ToUpperInvariant() switch
        {
            "B"  => num,
            "KB" => num * 1024L,
            "MB" => num * 1024L * 1024,
            "GB" => num * 1024L * 1024 * 1024,
            _    => 0
        };
    }

    private static string SizeToCli(string display)
    {
        if (string.IsNullOrWhiteSpace(display)) return "";
        var parts = display.Trim().Split(' ');
        if (parts.Length != 2) return display;
        return parts[1].ToUpperInvariant() switch
        {
            "B"  => parts[0],
            "KB" => $"{parts[0]}k",
            "MB" => $"{parts[0]}m",
            "GB" => $"{parts[0]}g",
            _    => display
        };
    }

    private static string SolidToCli(string display) => display switch
    {
        "" or null    => "",
        "非固实"       => "off",
        "固实"         => "on",
        _             => SizeToCli(display)
    };

    private static int LevelToInt(string s) => s.Length > 0 && char.IsDigit(s[0]) ? s[0] - '0' : 5;
    private static string LevelFromInt(int n) => Levels.FirstOrDefault(l => l[0] - '0' == n) ?? "5 - 普通";

    private static string ExtractVolumePreset(string preset)
    {
        if (string.IsNullOrWhiteSpace(preset)) return "";
        return preset.Split(' ', StringSplitOptions.RemoveEmptyEntries)[0].Trim();
    }

    private static string UnitToCliSuffix(string unit) => unit switch
    {
        "KB" => "k",
        "MB" => "m",
        "GB" => "g",
        _ => ""
    };

    private void SyncVolumeInput()
    {
        if (!IsVolumeEnabled || !IsVolumeSplitEnabled)
        {
            VolumeSize = "";
            return;
        }

        if (string.IsNullOrWhiteSpace(SelectedVolumeMode))
        {
            VolumeSize = "";
            return;
        }

        if (IsVolumePresetMode)
        {
            VolumeSize = ExtractVolumePreset(SelectedVolumePreset);
            return;
        }

        var raw = VolumeCustomSize.Trim();
        VolumeSize = string.IsNullOrWhiteSpace(raw) ? "" : $"{raw}{UnitToCliSuffix(SelectedVolumeUnit)}";
    }

    private bool IsVolumeInputValid(out string error)
    {
        error = "";

        if (string.IsNullOrWhiteSpace(VolumeSize))
            return true;

        if (IsVolumePresetMode)
            return true;

        if (!decimal.TryParse(VolumeCustomSize.Trim(), out var number) || number <= 0)
        {
            error = "分卷大小无效，请输入大于 0 的数字。";
            return false;
        }

        return true;
    }

    private void NormalizeMethodForLevel()
    {
        if (_updating || Methods.Count == 0) return;

        if (LevelToInt(SelectedLevel) == 0)
        {
            if (Methods.Contains("Copy") && SelectedMethod != "Copy")
                SelectedMethod = "Copy";
            return;
        }

        if (SelectedMethod == "Copy")
        {
            var preferred = SelectedFormat switch
            {
                "7z" => "LZMA2",
                "zip" => "Deflate",
                "gzip" => "Deflate",
                "bzip2" => "BZip2",
                "xz" => "LZMA2",
                _ => "Copy"
            };

            if (Methods.Contains(preferred))
                SelectedMethod = preferred;
        }
    }

    private void RefreshControlStates()
    {
        var level = LevelToInt(SelectedLevel);
        var isCopy = string.Equals(SelectedMethod, "Copy", StringComparison.OrdinalIgnoreCase);

        IsMethodEnabled = Methods.Count > 0;
        IsDictionaryEnabled = level > 0 && !isCopy && DictionarySizes.Count > 1;
        IsWordSizeEnabled = level > 0 && !isCopy && WordSizes.Count > 0;
        IsSolidEnabled = (SelectedFormat == "7z" || SelectedFormat == "xz") && level > 0 && !isCopy && SolidBlockSizes.Count > 0;
        IsThreadCountEnabled = ThreadOptions.Count > 1;
        IsSfxEnabled = SelectedFormat == "7z" && (SelectedMethod == "LZMA" || SelectedMethod == "LZMA2" || SelectedMethod == "Copy");

        if (!IsSfxEnabled && CreateSfx)
            CreateSfx = false;

        IsVolumeEnabled = !CreateSfx;
        if (!IsVolumeEnabled)
        {
            IsVolumeSplitEnabled = false;
            VolumeSize = "";
        }

        SyncVolumeInput();
        UpdateMemoryUsage();
    }

    private void AddExclusionItem(ExclusionItem item)
    {
        if (Exclusions.Any(existing => string.Equals(existing.Path, item.Path, StringComparison.OrdinalIgnoreCase)
            && existing.IsFolder == item.IsFolder
            && existing.IsRecursive == item.IsRecursive))
        {
            return;
        }

        Exclusions.Add(item);
    }

